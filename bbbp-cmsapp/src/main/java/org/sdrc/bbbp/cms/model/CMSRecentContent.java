package org.sdrc.bbbp.cms.model;

public class CMSRecentContent {
	
	private String content;
	private String lastUpdateViweName;
	private String lastUpdateOn;
	private String fileName;
	private String filePath;
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	public String getLastUpdateViweName() {
		return lastUpdateViweName;
	}
	public void setLastUpdateViweName(String lastUpdateViweName) {
		this.lastUpdateViweName = lastUpdateViweName;
	}
	public String getLastUpdateOn() {
		return lastUpdateOn;
	}
	public void setLastUpdateOn(String lastUpdateOn) {
		this.lastUpdateOn = lastUpdateOn;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	
	

}
