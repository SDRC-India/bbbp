package org.sdrc.bbbp.dashboard.models;

public class CSRTrendChartModel {
	
	
	private int timeNid;
	private String key;
	private String date;
	private Double value;
	private String name;
	
	
	public int getTimeNid() {
		return timeNid;
	}
	public void setTimeNid(int timeNid) {
		this.timeNid = timeNid;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public Double getValue() {
		return value;
	}
	public void setValue(Double value) {
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
